-- Manual cleanup helper for pre-launch data resets.
-- Usage:
--   select sa_clear_test_data('CLEAR_IS_DEPT_TEST_DATA');
--   select sa_clear_test_data('CLEAR_IS_DEPT_TEST_DATA', false); -- also clears location codes/account mappings
create or replace function sa_clear_test_data(
  confirm_text text,
  keep_reference_data boolean default true
)
returns void as $$
declare
  table_name text;
  truncate_list text := '';
  core_tables text[] := array[
    'sa_admin_action_logs',
    'sa_location_change_print_files',
    'sa_location_change_requests',
    'sa_subscription_cost_items',
    'sa_subscription_sold_items',
    'sa_subscription_cost_files',
    'sa_subscription_sold_files',
    'sa_development_notes',
    'sa_tasks',
    'sa_equipment',
    'sa_customers'
  ];
  reference_tables text[] := array[
    'sa_location_codes',
    'sa_location_account_numbers',
    'sa_location_sold_account_numbers',
    'sa_app_settings',
    'sa_xid_consultants'
  ];
begin
  if confirm_text <> 'CLEAR_IS_DEPT_TEST_DATA' then
    raise exception 'Confirmation text mismatch. Expected CLEAR_IS_DEPT_TEST_DATA';
  end if;

  foreach table_name in array core_tables loop
    if to_regclass(format('public.%I', table_name)) is not null then
      truncate_list := concat_ws(', ', truncate_list, format('public.%I', table_name));
    end if;
  end loop;

  if not keep_reference_data then
    foreach table_name in array reference_tables loop
      if to_regclass(format('public.%I', table_name)) is not null then
        truncate_list := concat_ws(', ', truncate_list, format('public.%I', table_name));
      end if;
    end loop;
  end if;

  if truncate_list <> '' then
    execute 'truncate table ' || truncate_list || ' restart identity cascade';
  end if;
end;
$$ language plpgsql;


