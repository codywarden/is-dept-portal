-- Reset activation upload numbers from admin tools.
create or replace function sa_reset_activation_upload_number(
  p_kind text,
  p_next_number bigint
)
returns void as $$
begin
  if p_next_number < 1 then
    raise exception 'p_next_number must be >= 1';
  end if;

  if p_kind = 'cost' then
    execute format(
      'alter table public.sa_subscription_cost_files alter column upload_number restart with %s',
      p_next_number
    );
  elsif p_kind = 'sold' then
    execute format(
      'alter table public.sa_subscription_sold_files alter column upload_number restart with %s',
      p_next_number
    );
  else
    raise exception 'Unsupported kind: %', p_kind;
  end if;
end;
$$ language plpgsql security definer;

